package com.kh.first; // 패키지 선언부
// 패키지 작성시 2단계 까지는 도메인의 역순으로 작성(관례)

public class HelloWorld { // 클래스 영역 시작부분
	
	/*
	 주석 여러줄 부분
	 */
	
	// 메인 메서드 -> 자바애플리케이션 실행하는 시작점 역할. entry point
	// 메인 메서드 -> public static void main(String args[]) 형태를 띄어야함
	
	public static void main(String args[]) {
		// 메서드 영역
		System.out.print("Hello World"); // 출력. 콘솔창에 () 안 문자열 화면에 출력함
		
	}

}
